num_int = int(input("Informe um número inteiro: "))
num_dec = float(input("Informe um número decimal: "))

print(f"O produto do dobro do inteiro com metade do decimal é {(num_int*2)*(num_dec/2)}")
print(f"A soma do triplo do inteiro com o decimal é {(num_int*3)+num_dec}")
print(f"O decimal elevado ao cubo é {num_dec**3}")